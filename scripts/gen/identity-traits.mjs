#!/usr/bin/env bun
/**
 * Generate per-trait pages for each identity schema.
 * One page per leaf field in each JSON Schema.
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync, rmSync } from "node:fs";
import { resolve, join } from "node:path";

const SCHEMAS = [
	{ slug: "ciam-default", path: "../platform/dev/ciam-kratos/identity.schema.json", title: "CIAM Default" },
	{ slug: "ciam-customer", path: "../platform/dev/ciam-kratos/customer.schema.json", title: "CIAM Customer" },
	{ slug: "ciam-company", path: "../platform/dev/ciam-kratos/company-identity.schema.json", title: "CIAM Company" },
	{ slug: "iam-admin", path: "../platform/dev/iam-kratos/admin-identity.schema.json", title: "IAM Admin" },
];

const OUT_DIR = "content/docs/reference/schemas/traits";

if (existsSync(OUT_DIR)) rmSync(OUT_DIR, { recursive: true, force: true });
mkdirSync(OUT_DIR, { recursive: true });

function walkLeaves(node, path = "", required = []) {
	const out = [];
	if (!node?.properties) return out;
	for (const [key, val] of Object.entries(node.properties)) {
		const fullPath = path ? `${path}.${key}` : key;
		const isRequired = required.includes(key);

		if (val.type === "object" && val.properties) {
			out.push(...walkLeaves(val, fullPath, val.required || []));
		} else {
			out.push({
				path: fullPath,
				type: val.type || "any",
				format: val.format,
				required: isRequired,
				title: val.title,
				description: val.description,
				oryExt: val["ory.sh/kratos"],
				enum: val.enum,
				pattern: val.pattern,
			});
		}
	}
	return out;
}

function slugify(s) {
	return s.replace(/\./g, "-");
}

const allTraits = [];
for (const s of SCHEMAS) {
	let schema;
	try {
		schema = JSON.parse(readFileSync(resolve(s.path), "utf-8"));
	} catch (e) {
		console.warn(`Skipping ${s.path}: ${e.message}`);
		continue;
	}

	// Navigate to traits property (Kratos identity schemas)
	const traitsRoot = schema.properties?.traits;
	if (!traitsRoot) continue;

	const leaves = walkLeaves(traitsRoot, "", traitsRoot.required || []);
	for (const l of leaves) {
		allTraits.push({ ...l, schema: s.slug, schemaTitle: s.title });
	}
}

// Per-trait pages
for (const t of allTraits) {
	const slug = `${t.schema}-${slugify(t.path)}`;
	let body = `---\ntitle: ${JSON.stringify(t.path)}\ndescription: ${JSON.stringify(`Trait in the ${t.schemaTitle} identity schema`)}\n---\n\n# Trait \`${t.path}\`\n\n`;
	body += `**Schema:** [${t.schemaTitle}](/docs/reference/schemas/${t.schema})\n\n`;
	body += `**Type:** \`${t.type}\`${t.format ? ` (format: \`${t.format}\`)` : ""}\n\n`;
	body += `**Required:** ${t.required ? "yes" : "no"}\n\n`;
	if (t.title) body += `**Label:** ${t.title}\n\n`;
	if (t.description) body += `## Description\n\n${t.description}\n\n`;
	if (t.pattern) body += `**Validation pattern:** \`${t.pattern}\`\n\n`;
	if (t.enum?.length) body += `**Allowed values:** ${t.enum.map((v) => `\`${v}\``).join(", ")}\n\n`;

	if (t.oryExt) {
		body += `## Kratos role\n\n`;
		if (t.oryExt.credentials?.password?.identifier) body += `- **Password identifier** — user logs in with this value.\n`;
		if (t.oryExt.credentials?.webauthn?.identifier) body += `- **WebAuthn identifier** — used as the username for WebAuthn challenges.\n`;
		if (t.oryExt.credentials?.totp?.account_name) body += `- **TOTP account name** — displayed in authenticator apps.\n`;
		if (t.oryExt.verification?.via) body += `- **Verifiable via** \`${t.oryExt.verification.via}\`.\n`;
		if (t.oryExt.recovery?.via) body += `- **Recovery via** \`${t.oryExt.recovery.via}\`.\n`;
		body += `\n`;
	}

	body += `## Where this is used\n\nThe trait appears in:\n- Registration form (auto-generated by Hera from the schema).\n- Settings flow (for updates).\n- ID token claims (Hydra includes traits).\n- Identity admin views in Athena.\n\n`;

	body += `\n---\n\n*Generated from \`platform/dev/${t.schema.startsWith("ciam") ? "ciam-kratos" : "iam-kratos"}/*.json\` at build time.*\n`;
	writeFileSync(join(OUT_DIR, `${slug}.mdx`), body);
}

// Overview
let overview = `---\ntitle: Identity traits\ndescription: ${JSON.stringify(`Every trait across all Olympus identity schemas (${allTraits.length} total)`)}\n---\n\nEach identity schema declares **traits** — fields stored on the identity (email, name, role, etc.). This page enumerates every trait across all schemas.\n\n`;
const bySchema = {};
for (const t of allTraits) (bySchema[t.schema] ||= []).push(t);
for (const schema of Object.keys(bySchema).sort()) {
	overview += `## ${schema}\n\n| Trait | Type | Required |\n|-------|------|----------|\n`;
	for (const t of bySchema[schema]) {
		const slug = `${t.schema}-${slugify(t.path)}`;
		overview += `| [\`${t.path}\`](./${slug}) | \`${t.type}\`${t.format ? ` (${t.format})` : ""} | ${t.required ? "yes" : "no"} |\n`;
	}
	overview += `\n`;
}
writeFileSync(join(OUT_DIR, "overview.mdx"), overview);

writeFileSync(
	join(OUT_DIR, "meta.json"),
	JSON.stringify({
		title: "Traits",
		pages: ["overview", ...allTraits.map((t) => `${t.schema}-${slugify(t.path)}`)],
	}, null, 2),
);

console.log(`Generated ${allTraits.length + 1} identity-trait pages in ${OUT_DIR}`);
