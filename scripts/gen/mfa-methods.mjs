#!/usr/bin/env bun
/**
 * Per-MFA-method deep-dive pages.
 */

import { existsSync, mkdirSync, rmSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const OUT_DIR = "content/docs/reference/mfa";

if (existsSync(OUT_DIR)) rmSync(OUT_DIR, { recursive: true, force: true });
mkdirSync(OUT_DIR, { recursive: true });

const METHODS = [
	{
		slug: "totp",
		name: "TOTP",
		fullName: "Time-based One-Time Password",
		rfc: "RFC 6238",
		aal: "AAL2",
		summary: "6-digit codes generated by an authenticator app (Google Authenticator, 1Password, Authy).",
		strengths: ["Wide app support", "Offline (no network needed for codes)", "Standard"],
		weaknesses: ["Phishable (user can be tricked into typing the code on a phishing site)", "Time-window sensitive (clock skew can cause failures)"],
		enrollment: "User scans a QR code or enters the secret manually.",
		recoveryStory: "If user loses the device, recovery codes (`lookup_secret`) or password reset are the fallbacks.",
	},
	{
		slug: "webauthn",
		name: "WebAuthn",
		fullName: "Web Authentication API",
		rfc: "W3C WebAuthn",
		aal: "AAL2 (AAL3 with attestation)",
		summary: "Cryptographic challenge-response with hardware (YubiKey, Touch ID) or passkeys.",
		strengths: ["Phishing-resistant (origin binding)", "User-friendly (biometric / touch)", "Strong cryptography"],
		weaknesses: ["Requires browser/OS support (now ubiquitous)", "Per-domain — passkeys don't migrate when you rename your domain"],
		enrollment: "Browser prompts user to use Touch ID / hardware key / save a passkey.",
		recoveryStory: "Multiple authenticators enrolled is the right pattern. Otherwise password reset.",
	},
	{
		slug: "lookup-secret",
		name: "Backup codes",
		fullName: "Lookup secrets / Recovery codes",
		rfc: "—",
		aal: "AAL2",
		summary: "Pre-generated one-time codes the user prints/saves. Each can be used exactly once.",
		strengths: ["Offline", "Works without any device"],
		weaknesses: ["User must safely store them", "Limited (typically 10-12 codes per generation)"],
		enrollment: "Generated all at once. User saves them.",
		recoveryStory: "These ARE the recovery story — paired with TOTP or WebAuthn as the user's break-glass.",
	},
	{
		slug: "code",
		name: "Email/SMS code",
		fullName: "One-time code via email or SMS",
		rfc: "—",
		aal: "Variable — often considered weaker than AAL2",
		summary: "Code sent to a verified channel (email or phone) and entered.",
		strengths: ["No app required", "Familiar UX"],
		weaknesses: ["SIM swap (SMS)", "Email compromise gives access", "Latency"],
		enrollment: "Implicit — the verified email/phone IS the channel.",
		recoveryStory: "User regains channel access via separate channel.",
		olympusNote:
			"Olympus's default doesn't include SMS (no SMS provider configured). Email-code is available via the recovery flow but typically not for ongoing MFA.",
	},
	{
		slug: "step-up",
		name: "Step-up auth",
		fullName: "AAL escalation",
		rfc: "—",
		aal: "AAL1 → AAL2 mid-session",
		summary: "User starts AAL1; for sensitive operations, Kratos requires AAL2 escalation before allowing.",
		strengths: ["Doesn't force MFA at every login", "Friction only when needed"],
		weaknesses: ["Requires careful integration in your app"],
		enrollment: "Same as the underlying methods (TOTP/WebAuthn).",
		recoveryStory: "Same as the underlying methods.",
		olympusNote: "Sensitive Kratos flows (settings, credential change) require AAL2 by default.",
	},
];

for (const m of METHODS) {
	let body = `---\ntitle: ${JSON.stringify(m.name)}\ndescription: ${JSON.stringify(m.summary)}\n---\n\n# ${m.fullName}\n\n**Spec:** ${m.rfc}\n\n**Provides AAL:** ${m.aal}\n\n## Summary\n\n${m.summary}\n\n## Strengths\n\n${m.strengths.map((s) => `- ${s}`).join("\n")}\n\n## Weaknesses\n\n${m.weaknesses.map((w) => `- ${w}`).join("\n")}\n\n## Enrollment\n\n${m.enrollment}\n\n## Recovery\n\n${m.recoveryStory}\n\n`;
	if (m.olympusNote) body += `## Olympus specifics\n\n${m.olympusNote}\n\n`;
	body += `## Related\n\n- [Identity — TOTP and WebAuthn](/docs/identity/totp-and-webauthn)\n- [Identity — MFA policy](/docs/identity/mfa-policy)\n- [Identity — Sessions, AAL, refresh](/docs/identity/sessions-aal-refresh)\n`;
	writeFileSync(join(OUT_DIR, `${m.slug}.mdx`), body);
}

let overview = `---\ntitle: MFA methods\ndescription: ${JSON.stringify("Multi-factor authentication methods supported by Olympus")}\n---\n\nOlympus supports four primary MFA methods plus a step-up mechanism.\n\n| Method | AAL | Phishing-resistant |\n|--------|-----|--------------------|\n`;
for (const m of METHODS) {
	const phishResistant =
		m.slug === "webauthn" ? "**Yes**" : m.slug === "totp" ? "no" : m.slug === "lookup-secret" ? "no" : m.slug === "code" ? "no" : "—";
	overview += `| [${m.name}](./${m.slug}) | ${m.aal} | ${phishResistant} |\n`;
}
overview += `\n**Recommendation:** WebAuthn for primary MFA, lookup-secret as fallback. See [Identity — MFA policy](/docs/identity/mfa-policy).\n`;
writeFileSync(join(OUT_DIR, "overview.mdx"), overview);

writeFileSync(join(OUT_DIR, "meta.json"), JSON.stringify({ title: "MFA methods", pages: ["overview", ...METHODS.map((m) => m.slug)] }, null, 2));

console.log(`Generated ${METHODS.length + 1} MFA method pages in ${OUT_DIR}`);
