#!/usr/bin/env bun
/**
 * Generate the env var catalog by grepping all repos for process.env.X and std::env::var("X").
 *
 * Output: site/content/docs/reference/env-vars.mdx — one page with a grouped table.
 */

import { readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { join, resolve } from "node:path";

const REPOS = [
	{ name: "athena", path: "../athena/src", pat: /process\.env\.([A-Z][A-Z0-9_]+)/g },
	{ name: "hera", path: "../hera/src", pat: /process\.env\.([A-Z][A-Z0-9_]+)/g },
	{ name: "sdk", path: "../sdk/src", pat: /process\.env\.([A-Z][A-Z0-9_]+)/g },
	{ name: "site", path: "../site/src", pat: /process\.env\.([A-Z][A-Z0-9_]+)/g },
	{ name: "octl", path: "../octl/src", pat: /process\.env\.([A-Z][A-Z0-9_]+)/g },
	{ name: "daedalus", path: "../daedalus/src-tauri/src", pat: /std::env::var\("([A-Z][A-Z0-9_]+)"\)/g },
];

const OUT_PATH = "content/docs/reference/env-vars.mdx";

function walk(dir) {
	const out = [];
	try {
		for (const e of readdirSync(dir)) {
			if (e === "node_modules" || e.startsWith(".")) continue;
			const p = join(dir, e);
			const st = statSync(p);
			if (st.isDirectory()) out.push(...walk(p));
			else if (st.isFile() && /\.(ts|tsx|js|mjs|cjs|rs)$/.test(e)) out.push(p);
		}
	} catch {
		// path may not exist (e.g. octl/src structure differs)
	}
	return out;
}

const byVar = {}; // VAR_NAME -> Set<repo>

for (const r of REPOS) {
	const files = walk(resolve(r.path));
	for (const f of files) {
		const content = readFileSync(f, "utf-8");
		const m = content.matchAll(r.pat);
		for (const match of m) {
			const v = match[1];
			(byVar[v] ||= new Set()).add(r.name);
		}
	}
}

const vars = Object.keys(byVar).sort();

const body = `---
title: Environment variables
description: Every environment variable referenced across every Olympus container
---

This catalog lists every environment variable referenced in Olympus source code. The presence of a checkmark in a column means that repo's source reads the variable.

The catalog is generated at build time by grepping \`process.env.X\` (TypeScript) and \`std::env::var("X")\` (Rust). If a variable is referenced but missing from this list, the catalog is stale — re-run \`bun run scripts/gen/env-vars.mjs\`.

## Catalog

| Variable | athena | hera | sdk | site | octl | daedalus |
|----------|:------:|:----:|:---:|:----:|:----:|:--------:|
${vars
	.map((v) => {
		const s = byVar[v];
		return `| \`${v}\` | ${s.has("athena") ? "✓" : ""} | ${s.has("hera") ? "✓" : ""} | ${s.has("sdk") ? "✓" : ""} | ${s.has("site") ? "✓" : ""} | ${s.has("octl") ? "✓" : ""} | ${s.has("daedalus") ? "✓" : ""} |`;
	})
	.join("\n")}

## Total count

${vars.length} unique environment variables.

---

*Generated by \`scripts/gen/env-vars.mjs\` at build time.*
`;

writeFileSync(OUT_PATH, body);
console.log(`Generated env-vars catalog with ${vars.length} variables at ${OUT_PATH}`);
