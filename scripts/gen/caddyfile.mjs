#!/usr/bin/env bun
/**
 * Caddyfile directive reference. Parse platform/prod/Caddyfile and emit a
 * single MDX page that lists every directive block with a short summary.
 *
 * The Caddyfile parser is intentionally minimal — it walks lines, tracks
 * brace depth, and groups directives by their host block.
 */

import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const SRC = "../platform/prod/Caddyfile";
const OUT = "content/docs/reference/config/caddyfile.mdx";

let raw;
try {
	raw = readFileSync(resolve(SRC), "utf-8");
} catch (e) {
	console.warn(`Caddyfile not found at ${SRC}: ${e.message}`);
	process.exit(0);
}

const lines = raw.split("\n");
const blocks = []; // { host, directives: [...] }
let cur = null;
let braceDepth = 0;
let globalBlock = { host: "(global)", directives: [] };
let inGlobal = false;

for (let i = 0; i < lines.length; i++) {
	const line = lines[i].replace(/#.*$/, "").trim();
	if (!line) continue;

	if (braceDepth === 0 && line.startsWith("{") && cur === null) {
		// Global block
		inGlobal = true;
		braceDepth++;
		continue;
	}

	if (braceDepth === 1 && inGlobal && line === "}") {
		inGlobal = false;
		braceDepth--;
		continue;
	}

	if (inGlobal) {
		globalBlock.directives.push(line);
		continue;
	}

	if (line.endsWith("{") && braceDepth === 0) {
		const host = line.slice(0, -1).trim();
		cur = { host, directives: [] };
		braceDepth++;
		continue;
	}

	if (line === "}" && braceDepth === 1) {
		if (cur) blocks.push(cur);
		cur = null;
		braceDepth--;
		continue;
	}

	if (cur && braceDepth >= 1) {
		// Track nested braces inside the host block (sub-directives)
		braceDepth += (line.match(/\{/g) || []).length;
		braceDepth -= (line.match(/\}/g) || []).length;
		cur.directives.push(line);
	}
}

let body = `---\ntitle: Caddyfile\ndescription: ${JSON.stringify("Production Caddy reverse-proxy configuration")}\n---\n\nThe production Caddyfile lives at \`platform/prod/Caddyfile\`. It is the **single source of truth** for HTTPS termination, reverse-proxy routing, rate limiting, and security headers in production. Modifications to this file are deployed by the platform deploy workflow.\n\nThis reference is generated by parsing the Caddyfile and listing every host block plus its directives. For the upstream Caddy directive documentation, see [caddyserver.com/docs/caddyfile/directives](https://caddyserver.com/docs/caddyfile/directives).\n\n`;

body += `## Global block\n\n`;
if (globalBlock.directives.length === 0) {
	body += `*No global directives (or none captured by the parser).*\n\n`;
} else {
	body += `\`\`\`text\n${globalBlock.directives.join("\n")}\n\`\`\`\n\n`;
}

body += `## Host blocks\n\n`;
body += `The file defines ${blocks.length} host blocks.\n\n`;

for (const b of blocks) {
	body += `### \`${b.host}\`\n\n`;
	body += `${b.directives.length} directives.\n\n`;
	body += `\`\`\`text\n${b.directives.join("\n")}\n\`\`\`\n\n`;
}

body += `---\n\n*Generated from \`${SRC.replace("../", "")}\` at build time.*\n`;
writeFileSync(OUT, body);
console.log(`Generated Caddyfile reference at ${OUT} (${blocks.length} host blocks, ${globalBlock.directives.length} global directives)`);
